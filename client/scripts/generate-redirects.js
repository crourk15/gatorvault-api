/**
 * Generate server/_redirects from client/lib/routes.js (single route source).
 */
const fs = require('fs');
const path = require('path');
const {
  REACT_REWRITES,
  ADMIN_AND_LEGACY_HTML,
  LEGACY_VAULT_TAB_REDIRECTS,
} = require('../lib/routes');

const serverDir = path.join(__dirname, '..', '..', 'server');
const outPath = path.join(serverDir, '_redirects');

function formatRule(rule) {
  const status = rule.status ?? 200;
  const force = rule.force ? '!' : '';
  return `${rule.from}  ${rule.to}  ${status}${force}`;
}

/** Legacy ?open=vault&tab=* query links → canonical React vault paths (301). */
function legacyVaultQueryRules() {
  const rules = [{ from: '/?open=vault', to: '/vault', status: 301 }];
  for (const [tab, dest] of Object.entries(LEGACY_VAULT_TAB_REDIRECTS)) {
    rules.push({
      from: `/?open=vault&tab=${tab}`,
      to: dest,
      status: 301,
    });
  }
  return rules;
}

const LEGACY_QUERY_RULES = legacyVaultQueryRules();

const lines = [
  '# Generated by client/scripts/generate-redirects.js — do not edit by hand',
  '# Re-run: node client/scripts/generate-redirects.js',
  '',
  '# API proxy',
  '/api/*  https://gatorvault-api.onrender.com/api/:splat  200',
  '',
  '# Legacy monolith vault query params → React vault routes',
  ...LEGACY_QUERY_RULES.map(formatRule),
  '',
  '# React UI (Next.js static export)',
  ...REACT_REWRITES.map(formatRule),
  '',
  '# Admin + legacy HTML',
  ...ADMIN_AND_LEGACY_HTML.map(formatRule),
  '',
];

fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log(
  '[generate-redirects] Wrote',
  outPath,
  `(${LEGACY_QUERY_RULES.length + REACT_REWRITES.length + ADMIN_AND_LEGACY_HTML.length + 1} rules)`
);
