/**
 * X AutoPoster quality gates — all checks must pass before a news post is queued or sent.
 */
const template = require('./x-autoposter-template');
const { TRUSTED_REPORTERS } = require('./content-validator');

const MAX_NEWS_AGE_MS = parseInt(process.env.X_AUTOPOST_MAX_NEWS_AGE_MS || String(3 * 60 * 60 * 1000), 10);
const MAX_BREAKING_AGE_MS = parseInt(
  process.env.X_AUTOPOST_MAX_BREAKING_AGE_MS || String(30 * 60 * 1000),
  10
);

const TRUSTED_OUTLETS = [
  'on3',
  '247sports',
  '247 sports',
  'rivals',
  'espn',
  'the athletic',
  'athletic',
  'floridagators.com',
  'gators.com',
  'uf football',
  'gators online',
  'onlygators',
  'gatorcountry',
  'sports illustrated',
  'si.com'
];

const GENERIC_CONTEXT_PATTERNS = [
  /^committed to florida\.?$/i,
  /^committed to florida per .+\.?$/i,
  /^flipped to florida\.?$/i,
  /^flipped to florida per .+\.?$/i,
  /^received an offer from florida\.?$/i,
  /^received an offer from florida per .+\.?$/i,
  /^scheduled an official visit to florida\.?$/i,
  /^scheduled an official visit to florida per .+\.?$/i,
  /^scheduled a visit to gainesville\.?$/i,
  /^scheduled a visit to gainesville per .+\.?$/i,
  /^entered the transfer portal\.?$/i,
  /^entered the transfer portal per .+\.?$/i,
  /^entered the transfer portal; florida is among the programs tracking\.?$/i,
  /^entered the transfer portal; florida is among the programs tracking per .+\.?$/i,
  /^picked up a uf prediction\.?$/i,
  /^picked up a florida prediction\.?$/i,
  /^picked up a florida futurecast prediction\.?$/i,
  /^picked up a florida prediction machine pick\.?$/i,
  /^cancelled his official visit to florida\.?$/i,
  /^cancelled his official visit to florida per .+\.?$/i,
  /^decommitted\.?$/i,
  /^decommitted from .+\.?$/i,
  /^decommitted from .+ per .+\.?$/i,
  /^news:\s*/i,
  /^breaking:\s*/i
];

const HEADLINE_ONLY_LINE_PATTERNS = [
  /^news:\s*.+$/i,
  /^breaking:\s*.+$/i,
  /^uf trending\b/i,
  /\btrending (?:for|to) florida\b/i,
  /^gators offered\b/i,
  /^florida offered\b/i,
  /^class of 20\d{2}\s+\d+★?\s+[A-Z]{1,4}\s+.+\s+—\s*$/i
];

const RANK_ONLY_INSIDER_RE = /^on3 ranks him no\. \d+ nationally\.?$/i;

const BARE_PREDICTION_INSIDER_RE =
  /^[A-Za-z .'-]{3,48} logged a florida (?:futurecast|prediction|rpm pick)(?:\s*\(\d{1,3}%\))?\.?$/i;

const AI_INVENTED_PATTERNS = [
  /\bas an ai\b/i,
  /\bi(?:'m| am) an ai\b/i,
  /\bchatgpt\b/i,
  /\bgenerated by ai\b/i,
  /\bfictional\b/i,
  /\bmade[- ]up\b/i,
  /\bunsourced rumor\b/i,
  /\bper my sources\b/i
];

/** Block weights for composite quality score (must sum to 1.0). */
const SCORE_WEIGHTS = {
  identity: 0.4,
  context: 0.3,
  insider: 0.3
};

const POSTING_THRESHOLD = parseInt(process.env.X_AUTOPOST_QUALITY_THRESHOLD || '85', 10);
const SOURCE_CONFIDENCE_REQUIRED = 100;

const HARD_SKIP_TYPES = new Set([
  'headline_only',
  'duplicate_sentences',
  'missing_identity',
  'missing_context',
  'missing_insider',
  'unverified_sourcing',
  'stale'
]);

function normalizeSentence(s) {
  return String(s || '')
    .toLowerCase()
    .replace(/["""'']/g, '')
    .replace(/\s+/g, ' ')
    .replace(/[.!?…]+$/g, '')
    .trim();
}

function extractBlocks(text) {
  return String(text || '')
    .split('\n')
    .map((l) => l.trim())
    .filter(Boolean);
}

function parseTemplateBlocks(item) {
  if (item.templateBlocks && typeof item.templateBlocks === 'object') {
    return {
      identity: String(item.templateBlocks.identity || '').trim(),
      context: String(item.templateBlocks.context || '').trim(),
      insider: String(item.templateBlocks.insider || '').trim()
    };
  }
  const lines = extractBlocks(item.text);
  return {
    identity: lines[0] || '',
    context: lines[1] || '',
    insider: lines.slice(2).join(' ') || ''
  };
}

function hasDuplicateSentences(text, blocks) {
  const b = blocks || parseTemplateBlocks({ text });
  const chunks = b.identity && b.context && b.insider ? [b.identity, b.context, b.insider] : [text];
  const seen = new Set();
  for (const chunk of chunks.filter(Boolean)) {
    for (const sentence of template.extractSentences(chunk)) {
      const norm = normalizeSentence(sentence);
      if (norm.length < 18) continue;
      if (seen.has(norm)) return true;
      seen.add(norm);
    }
  }
  const normContext = normalizeSentence(b.context);
  const normInsider = normalizeSentence(b.insider);
  if (normContext && normInsider && normContext === normInsider) return true;
  return false;
}

function isGenericSyntheticContext(contextLine) {
  const t = template.stripEmojisHashtags(contextLine || '').trim();
  if (!t) return true;
  if (t.length < 28) return true;
  return GENERIC_CONTEXT_PATTERNS.some((re) => re.test(t));
}

function isHeadlineOnlyLine(line) {
  const t = template.stripEmojisHashtags(line || '').trim();
  if (!t) return true;
  if (HEADLINE_ONLY_LINE_PATTERNS.some((re) => re.test(t))) return true;
  if (template.HEADLINE_ONLY_RE.test(t)) return true;
  if (t.length < 35 && !template.FACTUAL_SIGNAL_RE.test(t)) return true;
  return false;
}

function isRankOnlyInsider(insiderLine) {
  const t = template.stripEmojisHashtags(insiderLine || '').trim();
  return RANK_ONLY_INSIDER_RE.test(t) || BARE_PREDICTION_INSIDER_RE.test(t);
}

function hasHumanReportedContext(contextLine, meta = {}) {
  const context = template.stripEmojisHashtags(contextLine || '').trim();
  if (!context || isGenericSyntheticContext(context)) return false;
  if (isHeadlineOnlyLine(context)) return false;

  if (meta.contextFromBeat || meta.contextFromIntel) {
    return context.length >= 28;
  }

  const beatText = String(meta.beatText || '').trim();
  const intelDetail = template.stripEmojisHashtags(meta.intelDetail || '').trim();
  const fromBeat = beatText.length >= 30 && template.extractSentences(beatText).some(
    (s) => normalizeSentence(s) === normalizeSentence(context) || context.includes(s.slice(0, 40))
  );
  const fromIntel = intelDetail.length >= 28 && !/trending|momentum/i.test(intelDetail);
  const hasSubstance =
    template.FACTUAL_SIGNAL_RE.test(context) ||
    template.INSIDER_SIGNAL_RE.test(context) ||
    /\b(per|via|according to|told|said|reports|confirmed|scheduled|visited|contacted|quote)\b/i.test(context);

  if (fromBeat || fromIntel) return true;
  if (hasSubstance && context.length >= 40) return true;
  return false;
}

function hasVerifiedInsiderAngle(insiderLine, meta = {}) {
  const insider = template.stripEmojisHashtags(insiderLine || '').trim();
  if (!insider || isRankOnlyInsider(insider)) return false;
  if (insider.length < 20) return false;

  const contextNorm = normalizeSentence(meta.contextLine || '');
  if (normalizeSentence(insider) === contextNorm) return false;

  if (meta.insiderFromAnalyst === true) return true;
  if (meta.predictionPost === true && insider.length >= 28) return true;
  if (meta.insiderFromBeat === true) return true;
  if (meta.insiderFromIntel === true) return true;
  if (meta.insiderFromScouting === true) return true;
  if (meta.insiderFromBreakdown === true) return true;

  const hasAnalystCue =
    /\b(analyst|futurecast|prediction machine|rpm|scout|evaluator|staff|coach(?:es)?)\b/i.test(insider) ||
    /^[A-Z][a-z]+(?:\s+[A-Z][a-z]+){0,2}:/.test(insider);
  const hasInsiderSignal = template.INSIDER_SIGNAL_RE.test(insider);

  return hasAnalystCue && (hasInsiderSignal || insider.length >= 35);
}

function validateIdentityFields(ctx, blocks) {
  const identity = blocks?.identity || '';
  const name = String(ctx?.name || '').trim();
  const pos = String(ctx?.pos || ctx?.position || '').trim();
  const classYear = ctx?.classYear != null ? Number(ctx.classYear) : null;
  const school = String(ctx?.school || ctx?.formerSchool || '').trim();
  const natlRank = ctx?.natlRank != null ? Number(ctx.natlRank) : null;
  const starsLabel = ctx?.starsLabel || template.formatStarsLabel(ctx?.stars);
  const isPortal = ctx?.isPortal || /^portal\b/i.test(identity);

  const errors = [];
  if (!name || name.length < 4) {
    errors.push({ rule: 'identity', field: 'name', message: 'Identity block requires player name.' });
  }
  if (!pos) {
    errors.push({ rule: 'identity', field: 'pos', message: 'Identity block requires position.' });
  }
  if (!isPortal && (!classYear || Number.isNaN(classYear))) {
    errors.push({ rule: 'identity', field: 'classYear', message: 'Identity block requires class year.' });
  }
  if (!school) {
    errors.push({ rule: 'identity', field: 'school', message: 'Identity block requires school.' });
  }
  if (!isPortal) {
    const hasRank = natlRank > 0 || identity.includes('On3 #') || !!starsLabel;
    if (!hasRank) {
      errors.push({
        rule: 'identity',
        field: 'ranking',
        message: 'Identity block requires ranking (On3 national rank preferred).'
      });
    }
  }
  if (!identity) {
    errors.push({ rule: 'identity', field: 'identity', message: 'Identity block missing from post.' });
  }
  return errors;
}

function isPublicHttpUrl(url) {
  if (!url || typeof url !== 'string') return false;
  try {
    const u = new URL(url.trim());
    return u.protocol === 'http:' || u.protocol === 'https:';
  } catch {
    return false;
  }
}

function normalizeSource(raw) {
  if (!raw) return null;
  if (typeof raw === 'string') {
    const s = raw.trim();
    if (!s) return null;
    if (isPublicHttpUrl(s)) return { url: s, outlet: null, label: s };
    return { url: null, outlet: s, label: s };
  }
  const url = raw.url || raw.href || null;
  const outlet = raw.outlet || raw.name || raw.label || raw.reporter || null;
  const label = raw.label || [raw.reporter, outlet].filter(Boolean).join(' / ') || url;
  if (!url && !outlet) return null;
  return { url: url || null, outlet: outlet || null, label: String(label).trim() };
}

function sourceIsTrusted(source) {
  const norm = normalizeSource(source);
  if (!norm) return false;
  if (norm.url) {
    const lower = norm.url.toLowerCase();
    if (/paywall|subscriber-only|members-only|\/subscribe/.test(lower)) return false;
    if (TRUSTED_OUTLETS.some((o) => lower.includes(o.replace(/\s+/g, '')) || lower.includes(o))) return true;
    if (isPublicHttpUrl(norm.url)) return true;
  }
  const text = [norm.outlet, norm.label].filter(Boolean).join(' ').toLowerCase();
  if (AI_INVENTED_PATTERNS.some((re) => re.test(text))) return false;
  if (TRUSTED_REPORTERS.some((name) => text.includes(name.toLowerCase()))) return true;
  return TRUSTED_OUTLETS.some((o) => text.includes(o));
}

function validateVerifiedSources(sources, item) {
  const errors = [];
  const list = (sources || []).map(normalizeSource).filter(Boolean);
  if (!list.length) {
    errors.push({
      rule: 'sourcing',
      type: 'missing_sources',
      message: 'Post requires at least one verified human source (On3, 247, Rivals, ESPN, beat writers, or UF official).'
    });
    return errors;
  }
  if (!list.some(sourceIsTrusted)) {
    errors.push({
      rule: 'sourcing',
      type: 'untrusted_sources',
      message: 'Source must be human, trusted, and not paywalled or AI-generated.'
    });
  }
  const combined = [item.text, ...(list.map((s) => s.label || ''))].join('\n');
  if (AI_INVENTED_PATTERNS.some((re) => re.test(combined))) {
    errors.push({
      rule: 'sourcing',
      type: 'ai_invented',
      message: 'Post text or source appears AI-generated or unsourced.'
    });
  }
  return errors;
}

function resolveEventTimestamp(item) {
  const candidates = [
    item.sourceEventCreatedAt,
    item.sourcePublishedAt,
    item.eventTimestamp,
    item.intelTimestamp,
    item.createdAt
  ];
  for (const raw of candidates) {
    if (!raw) continue;
    const ms = new Date(raw).getTime();
    if (!Number.isNaN(ms)) return ms;
  }
  return null;
}

function validateFreshness(item, now = Date.now()) {
  const errors = [];
  const eventMs = resolveEventTimestamp(item);
  if (eventMs == null) {
    if (String(item.category || '').toLowerCase() === 'news' && String(item.source || '').startsWith('auto:')) {
      errors.push({
        rule: 'freshness',
        type: 'missing_timestamp',
        message: 'News posts require a source event timestamp for freshness validation.'
      });
    }
    return errors;
  }
  const cadence = require('./x-autoposter-cadence');
  const urgency = cadence.classifyItemUrgency(item);
  const maxAge = urgency.tier === 'breaking' || urgency.tier === 'urgent' ? MAX_BREAKING_AGE_MS : MAX_NEWS_AGE_MS;
  const ageMs = now - eventMs;
  if (ageMs > maxAge) {
    errors.push({
      rule: 'freshness',
      type: 'stale',
      message: `Source event is too old (${Math.round(ageMs / 60000)}m; max ${Math.round(maxAge / 60000)}m for ${urgency.tier}).`
    });
  }
  return errors;
}

function scoreIdentityBlock(ctx, blocks) {
  const identity = blocks?.identity || '';
  const name = String(ctx?.name || '').trim();
  const pos = String(ctx?.pos || ctx?.position || '').trim();
  const classYear = ctx?.classYear != null ? Number(ctx.classYear) : null;
  const school = String(ctx?.school || ctx?.formerSchool || '').trim();
  const natlRank = ctx?.natlRank != null ? Number(ctx.natlRank) : null;
  const starsLabel = ctx?.starsLabel || template.formatStarsLabel(ctx?.stars);
  const isPortal = ctx?.isPortal || /^portal\b/i.test(identity);

  let score = 0;
  const fields = [];

  if (name && name.length >= 4) {
    score += 25;
    fields.push('name');
  }
  if (pos) {
    score += 20;
    fields.push('pos');
  }
  if (isPortal || (classYear && !Number.isNaN(classYear))) {
    score += 20;
    fields.push('classYear');
  }
  if (school) {
    score += 20;
    fields.push('school');
  }
  const hasRank = natlRank > 0 || identity.includes('On3 #') || !!starsLabel;
  if (isPortal || hasRank) {
    score += 15;
    fields.push('ranking');
  }
  if (identity.length >= 12) {
    score = Math.min(100, score + 5);
  }

  return { score: Math.min(100, score), fields, complete: score >= 100 };
}

function scoreContextBlock(blocks, meta = {}) {
  const context = template.stripEmojisHashtags(blocks?.context || '').trim();
  if (!context) return { score: 0, complete: false };

  let score = 30;
  if (!isGenericSyntheticContext(context)) score += 25;
  if (hasHumanReportedContext(context, meta)) score += 30;
  if (context.length >= 40) score += 10;
  if (meta.contextFromBeat || meta.contextFromIntel) score += 5;

  return { score: Math.min(100, score), complete: score >= 85 && hasHumanReportedContext(context, meta) };
}

function scoreInsiderBlock(blocks, meta = {}) {
  const insider = template.stripEmojisHashtags(blocks?.insider || '').trim();
  if (!insider) return { score: 0, complete: false };

  let score = 25;
  if (!isRankOnlyInsider(insider)) score += 25;
  if (hasVerifiedInsiderAngle(insider, { ...meta, contextLine: blocks?.context })) score += 35;
  if (normalizeSentence(insider) !== normalizeSentence(blocks?.context || '')) score += 15;

  return { score: Math.min(100, score), complete: score >= 85 && hasVerifiedInsiderAngle(insider, { ...meta, contextLine: blocks?.context }) };
}

function computeSourceConfidence(sources, item) {
  const list = (sources || []).map(normalizeSource).filter(Boolean);
  if (!list.length) return { score: 0, pass: false };
  const trustedCount = list.filter(sourceIsTrusted).length;
  const allTrusted = trustedCount === list.length && trustedCount > 0;
  const combined = [item?.text, ...list.map((s) => s.label || '')].join('\n');
  const aiFlag = AI_INVENTED_PATTERNS.some((re) => re.test(combined));
  if (aiFlag || !allTrusted) return { score: 0, pass: false };
  return { score: SOURCE_CONFIDENCE_REQUIRED, pass: true };
}

function collectHardSkipReasons(item, blocks, meta) {
  const skips = [];

  if (!blocks.identity) {
    skips.push({ type: 'missing_identity', message: 'Identity block missing — automatic skip.' });
  }
  if (!blocks.context) {
    skips.push({ type: 'missing_context', message: 'Context block missing — automatic skip.' });
  }
  if (!blocks.insider) {
    skips.push({ type: 'missing_insider', message: 'Insider angle block missing — automatic skip.' });
  }

  if (hasDuplicateSentences(item.text, blocks)) {
    skips.push({ type: 'duplicate_sentences', message: 'Duplicate sentences detected — automatic skip.' });
  }

  const combined = [blocks.identity, blocks.context, blocks.insider].filter(Boolean).join('\n');
  if (combined && (isHeadlineOnlyLine(combined) || template.isHeadlineOnlyPost(combined))) {
    skips.push({ type: 'headline_only', message: 'Headline-only content — automatic skip.' });
  }

  const prediction = require('./x-autoposter-prediction');
  if (blocks.insider && prediction.isBarePredictionLine(blocks.insider)) {
    skips.push({ type: 'headline_only', message: 'Bare prediction line — automatic skip.' });
  }
  if (blocks.context && prediction.isBarePredictionLine(blocks.context)) {
    skips.push({ type: 'headline_only', message: 'Bare prediction context — automatic skip.' });
  }
  if (meta.predictionPost && blocks.identity && !/\(\s*.+,\s*.+\)/.test(blocks.identity)) {
    skips.push({ type: 'missing_identity', message: 'Prediction post missing school/hometown identity.' });
  }

  const sourceCheck = computeSourceConfidence(item.sources, item);
  if (!sourceCheck.pass) {
    skips.push({
      type: 'unverified_sourcing',
      message: 'Source confidence must be 100% from verified analysts — automatic skip.'
    });
  }

  const freshnessErrors = validateFreshness(item);
  if (freshnessErrors.length) {
    skips.push({
      type: 'stale',
      message: freshnessErrors[0].message || 'Stale content — automatic skip.'
    });
  }

  return { skips, sourceConfidence: sourceCheck.score, sourcePass: sourceCheck.pass };
}

/**
 * Percentage-based validation model:
 * Identity 40% · Context 30% · Insider 30% · Source confidence 100% required · 85% threshold.
 */
function scoreNewsPost(item) {
  const category = String(item.category || 'news').toLowerCase();
  if (category !== 'news') {
    return {
      pass: true,
      score: 100,
      threshold: POSTING_THRESHOLD,
      sourceConfidence: SOURCE_CONFIDENCE_REQUIRED,
      breakdown: null,
      hardSkips: [],
      errors: []
    };
  }

  const blocks = parseTemplateBlocks(item);
  const meta = item.validationMeta || {};
  const ctx = item.playerContext || meta.playerContext || {};
  const { skips, sourceConfidence, sourcePass } = collectHardSkipReasons(item, blocks, meta);

  const identity = scoreIdentityBlock(ctx, blocks);
  const context = scoreContextBlock(blocks, meta);
  const insider = scoreInsiderBlock(blocks, meta);

  const compositeScore = Math.round(
    identity.score * SCORE_WEIGHTS.identity +
      context.score * SCORE_WEIGHTS.context +
      insider.score * SCORE_WEIGHTS.insider
  );

  const breakdown = {
    identity: {
      weight: SCORE_WEIGHTS.identity,
      blockScore: identity.score,
      weighted: Math.round(identity.score * SCORE_WEIGHTS.identity * 10) / 10,
      fields: identity.fields
    },
    context: {
      weight: SCORE_WEIGHTS.context,
      blockScore: context.score,
      weighted: Math.round(context.score * SCORE_WEIGHTS.context * 10) / 10
    },
    insider: {
      weight: SCORE_WEIGHTS.insider,
      blockScore: insider.score,
      weighted: Math.round(insider.score * SCORE_WEIGHTS.insider * 10) / 10
    },
    compositeScore,
    threshold: POSTING_THRESHOLD,
    sourceConfidence,
    sourceConfidenceRequired: SOURCE_CONFIDENCE_REQUIRED
  };

  const errors = [];
  if (skips.length) {
    errors.push(...skips.map((s) => ({ rule: 'hard_skip', type: s.type, message: s.message })));
  }
  if (!sourcePass) {
    errors.push({
      rule: 'sourcing',
      type: 'source_confidence',
      message: `Source confidence ${sourceConfidence}% — requires ${SOURCE_CONFIDENCE_REQUIRED}%.`
    });
  }
  if (compositeScore < POSTING_THRESHOLD) {
    errors.push({
      rule: 'score',
      type: 'below_threshold',
      message: `Quality score ${compositeScore}% is below ${POSTING_THRESHOLD}% posting threshold.`
    });
  }

  const pass = skips.length === 0 && sourcePass && compositeScore >= POSTING_THRESHOLD;

  return {
    pass,
    valid: pass,
    score: compositeScore,
    threshold: POSTING_THRESHOLD,
    sourceConfidence,
    sourcePass,
    breakdown,
    blocks,
    hardSkips: skips,
    errors
  };
}

function validateNewsPostQuality(item) {
  const scored = scoreNewsPost(item);
  return {
    valid: scored.pass,
    score: scored.score,
    threshold: scored.threshold,
    sourceConfidence: scored.sourceConfidence,
    breakdown: scored.breakdown,
    hardSkips: scored.hardSkips,
    errors: scored.errors,
    blocks: scored.blocks
  };
}

/** Gate news candidates before content-mix category selection. */
function passesNewsQualityGate(item) {
  const category = String(item.category || 'news').toLowerCase();
  if (category !== 'news') return { pass: true, scored: null };
  const scored = scoreNewsPost(item);
  return { pass: scored.pass, scored };
}

