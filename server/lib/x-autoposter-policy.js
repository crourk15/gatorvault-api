/**
 * GatorVault X AutoPoster — content policy, sourcing rules, 50/30/20 mix.
 * See docs/x-autoposter-content-directive.md
 */
const { TRUSTED_REPORTERS } = require('./content-validator');

const SITE_URL = process.env.SITE_URL || 'https://gatorvaultinsider.com';

/** Target mix: value → engagement → promotion */
const CONTENT_MIX = {
  news: 0.5,
  engagement: 0.3,
  promo: 0.2
};

const CATEGORIES = Object.keys(CONTENT_MIX);
const ACTIONS = ['post', 'reply', 'quote'];

const NEWS_TOPICS = ['recruiting', 'portal', 'staff', 'game_week', 'roster', 'general'];

const AI_INVENTED_PATTERNS = [
  /\bas an ai\b/i,
  /\bi(?:'m| am) an ai\b/i,
  /\bchatgpt\b/i,
  /\bgenerated by ai\b/i,
  /\bfictional\b/i,
  /\bmade[- ]up\b/i,
  /\bunsourced rumor\b/i,
  /\bper my sources\b/i
];

const PROMO_MARKERS = [/gatorvault/i, /gatorvaultinsider\.com/i];

function isPublicHttpUrl(url) {
  if (!url || typeof url !== 'string') return false;
  try {
    const u = new URL(url.trim());
    return u.protocol === 'http:' || u.protocol === 'https:';
  } catch {
    return false;
  }
}

function normalizeSource(raw) {
  if (!raw) return null;
  if (typeof raw === 'string') {
    const s = raw.trim();
    if (!s) return null;
    if (isPublicHttpUrl(s)) return { url: s, outlet: null, label: s };
    return { url: null, outlet: s, label: s };
  }
  const url = raw.url || raw.href || null;
  const outlet = raw.outlet || raw.name || raw.label || raw.reporter || null;
  const label = raw.label || [raw.reporter, outlet].filter(Boolean).join(' / ') || url;
  if (!url && !outlet) return null;
  return { url: url || null, outlet: outlet || null, label: String(label).trim() };
}

function sourceMatchesTrusted(source) {
  const norm = normalizeSource(source);
  if (!norm) return false;
  if (norm.url && isPublicHttpUrl(norm.url)) {
    const lower = norm.url.toLowerCase();
    if (/paywall|subscriber-only|members-only/.test(lower)) return false;
    return true;
  }
  const text = [norm.outlet, norm.label].filter(Boolean).join(' ');
  return TRUSTED_REPORTERS.some((name) => text.toLowerCase().includes(name.toLowerCase()));
}

function containsInventedClaims(text) {
  return AI_INVENTED_PATTERNS.some((re) => re.test(String(text || '')));
}

function looksLikeFactualNews(text) {
  const t = String(text || '');
  return /\b(commit|portal|transfer|signs|hired|fired|injury|depth chart|recruit|verb|offer|flip|decommit|score|final|vs\.|@\w)/i.test(t);
}

function validatePostContent(item) {
  const errors = [];
  const category = String(item.category || 'news').toLowerCase();
  const action = String(item.action || 'post').toLowerCase();
  const text = String(item.text || '').trim();
  const sources = (item.sources || []).map(normalizeSource).filter(Boolean);

  if (!CATEGORIES.includes(category)) {
    errors.push({ field: 'category', message: `category must be one of: ${CATEGORIES.join(', ')}` });
  }
  if (!ACTIONS.includes(action)) {
    errors.push({ field: 'action', message: `action must be one of: ${ACTIONS.join(', ')}` });
  }
  if (!text) {
    errors.push({ field: 'text', message: 'Post text required' });
  }
  if (text.length > 280) {
    errors.push({ field: 'text', message: 'Post text exceeds 280 characters' });
  }

  if (containsInventedClaims(text)) {
    errors.push({
      field: 'text',
      type: 'ai_invented',
      message: 'Posts must not contain AI-invented or unsourced rumor language. Cite credible public reporting.'
    });
  }

  if (category === 'news') {
    if (!sources.length) {
      errors.push({
        field: 'sources',
        type: 'missing_sources',
        message:
          'News posts must cite at least one credible source (On3, beat writers, official team site, or public URL).'
      });
    } else if (!sources.some(sourceMatchesTrusted)) {
      errors.push({
        field: 'sources',
        type: 'untrusted_sources',
        message: 'News sources must be trusted public outlets or named beat writers — no paywalled reproduction.'
      });
    }
    if (looksLikeFactualNews(text) && !sources.some((s) => s.url)) {
      errors.push({
        field: 'sources',
        type: 'url_recommended',
        message: 'Factual news posts should include a public source URL when available.'
      });
    }
  }

  if (category === 'engagement') {
    if (action === 'reply' && !item.inReplyToStatusId) {
      errors.push({
        field: 'inReplyToStatusId',
        message: 'Engagement replies require inReplyToStatusId (tweet id to reply to).'
      });
    }
    if (action === 'quote' && !item.quoteTweetUrl && !item.quoteTweetId) {
      errors.push({
        field: 'quoteTweetUrl',
        message: 'Quote tweets require quoteTweetUrl or quoteTweetId.'
      });
    }
    if (looksLikeFactualNews(text) && !sources.length) {
      errors.push({
        field: 'sources',
        message: 'Engagement posts that state facts must still cite a credible source.'
      });
    }
  }

  if (category === 'promo') {
    const hasPromoMarker = PROMO_MARKERS.some((re) => re.test(text));
    if (!hasPromoMarker && !String(item.promoLink || '').includes('gatorvault')) {
      errors.push({
        field: 'text',
        type: 'promo_marker',
        message: `Promo posts should mention GatorVault or link to ${SITE_URL} — blend value, do not spam.`
      });
    }
  }

  return {
    valid: errors.filter((e) => e.type !== 'url_recommended').length === 0,
    errors,
    warnings: errors.filter((e) => e.type === 'url_recommended')
  };
}

function getSentPosts(items, { limit = 50, sinceDays = 14 } = {}) {
  const since = Date.now() - sinceDays * 24 * 60 * 60 * 1000;
  return items
    .filter((i) => i.status === 'sent' && i.sentAt && new Date(i.sentAt).getTime() >= since)
    .sort((a, b) => new Date(b.sentAt) - new Date(a.sentAt))
    .slice(0, limit);
}

function computeMixStats(sentItems) {
  const counts = { news: 0, engagement: 0, promo: 0, other: 0 };
  sentItems.forEach((i) => {
    const cat = CATEGORIES.includes(i.category) ? i.category : 'other';
    counts[cat] += 1;
  });
  const total = sentItems.length || 0;
  const actual = {};
  CATEGORIES.forEach((c) => {
    actual[c] = total ? Math.round((counts[c] / total) * 1000) / 10 : 0;
  });
  const target = {};
  CATEGORIES.forEach((c) => {
    target[c] = CONTENT_MIX[c] * 100;
  });
  const drift = {};
  CATEGORIES.forEach((c) => {
    drift[c] = Math.round((actual[c] - target[c]) * 10) / 10;
  });
  const underrepresented = [...CATEGORIES].sort(
    (a, b) => actual[a] - target[a] - (actual[b] - target[b])
  )[0];

  return {
    window: sentItems.length,
    counts,
    targetPercent: target,
    actualPercent: actual,
    driftPercent: drift,
    suggestedNextCategory: total < 3 ? 'news' : underrepresented
  };
}

function getContentPolicy() {
  return {
    mission: 'Credible sourced Gator news + real-time engagement + natural GatorVault promotion',
    tone: 'fan-first, authoritative, trustworthy',
    authPosting: 'oauth1_user_context',
    beatStream: 'bearer_read_only',
    contentMix: CONTENT_MIX,
    contentMixLabel: '50% news · 30% engagement · 20% promo',
    categories: CATEGORIES,
    actions: ACTIONS,
    newsTopics: NEWS_TOPICS,
    trustedSources: TRUSTED_REPORTERS,
    siteUrl: SITE_URL,
    rules: [
      'No AI-invented news or unsourced factual claims',
      'News posts require credible sources',
      'Engagement: replies, quotes, hype — facts still need sources',
      'Promo: blend GatorVault into the news cycle; value → engagement → promotion',
      'OAuth 1.0a for all posting; Bearer read-only for beat stream'
    ]
  };
}

module.exports = {
  CONTENT_MIX,
  CATEGORIES,
  ACTIONS,
  NEWS_TOPICS,
  normalizeSource,
  sourceMatchesTrusted,
  validatePostContent,
  getSentPosts,
  computeMixStats,
  getContentPolicy
};
