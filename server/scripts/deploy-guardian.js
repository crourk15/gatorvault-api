#!/usr/bin/env node
/**
 * Deployment Guardian — pre-deploy gate (build, routing, API probes).
 *
 * Usage:
 *   node scripts/deploy-guardian.js --phase=pre
 *   node scripts/deploy-guardian.js --phase=pre --static   # file checks only (Netlify post-build)
 *   node scripts/deploy-guardian.js --phase=pre --api        # include live API probes
 */
require('dotenv').config({ path: require('path').join(__dirname, '..', '.env') });

const fs = require('fs');
const path = require('path');
const { verifyPlatformWiring } = require('../lib/guardian/platform-wiring');
const { verifyBlueprints } = require('../lib/guardian/blueprint-validator');
const { REQUIRED_VAULT_EXPORTS } = require('../../client/lib/routes');
const {
  MONOLITH_ARCHIVE_HTML,
  FORBIDDEN_IN_ROOT_INDEX,
  isReactMarketingIndex,
} = require('../lib/guardian/monolith-archive');
const { verifyChunkAssets } = require('../../client/scripts/verify-chunk-assets');

const SERVER_ROOT = path.join(__dirname, '..');
const STATIC_ONLY = process.argv.includes('--static');
const PROBE_API = process.argv.includes('--api') || (!STATIC_ONLY && process.env.DEPLOY_GUARDIAN_PROBE_API !== 'false');

const REQUIRED_HTML_ROUTES = [
  'index.html',
  ...REQUIRED_VAULT_EXPORTS,
  'players/index.html',
  'portal/index.html',
  'futurecast/index.html',
  'scouting/index.html',
  '_next/static',
  'build-manifest.json',
  MONOLITH_ARCHIVE_HTML,
];

/** Vault paths must not rewrite to the monolith overlay. */
const FORBIDDEN_VAULT_MONOLITH = [
  '/vault /index.html',
  '/vault/ /index.html',
  '/vault/depth-chart /index.html',
  '/vault/recruiting /index.html',
  '/vault/portal /index.html',
  '/vault/film-room /index.html',
  '/vault/game-week /index.html',
  '/vault/live-feed /index.html',
  '/vault/staff /index.html',
];

const CORE_API_PROBES = [
  { id: 'futurecast-home', path: '/api/futurecast/home' },
  { id: 'portal-players', path: '/api/portal/players?limit=5' },
  { id: 'recruiting-board', path: '/api/recruiting/board' },
  { id: 'roster-players', path: '/api/roster/players' },
  { id: 'articles-published', path: '/api/articles/published' },
  { id: 'film-room-catalog', path: '/api/film-room/catalog' },
  { id: 'health', path: '/api/health' },
];

async function timedFetch(url, timeoutMs = 20000) {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), timeoutMs);
  try {
    const res = await fetch(url, { signal: controller.signal });
    clearTimeout(timer);
    let body = null;
    try {
      body = await res.json();
    } catch {
      body = null;
    }
    return { ok: res.ok, status: res.status, body };
  } catch (err) {
    clearTimeout(timer);
    return { ok: false, status: 0, error: err.message };
  }
}

function checkStaticRoutes() {
  const errors = [];
  for (const rel of REQUIRED_HTML_ROUTES) {
    const full = path.join(SERVER_ROOT, rel);
    if (!fs.existsSync(full)) {
      errors.push(`[routing] missing export: ${rel}`);
    }
  }

  const redirectsPath = path.join(SERVER_ROOT, '_redirects');
  if (fs.existsSync(redirectsPath)) {
    const text = fs.readFileSync(redirectsPath, 'utf8');
    if (!text.includes('/vault/futurecast')) errors.push('[routing] _redirects missing /vault/futurecast');
    if (!text.includes('/vault/portal/player')) errors.push('[routing] _redirects missing /vault/portal/player');
    if (!text.includes('/vault/scouting')) errors.push('[routing] _redirects missing /vault/scouting');
    if (!text.includes('/vault/players/')) errors.push('[routing] _redirects missing /vault/players');
    if (!text.includes('/join')) errors.push('[routing] _redirects missing /join');
    for (const forbidden of FORBIDDEN_VAULT_MONOLITH) {
      if (text.includes(forbidden)) {
        errors.push(`[routing] vault route still points at monolith: ${forbidden}`);
      }
    }
    if (!text.includes('Generated by client/scripts/generate-redirects.js')) {
      errors.push('[routing] _redirects not generated — run node client/scripts/generate-redirects.js');
    }
  } else {
    errors.push('[routing] server/_redirects missing');
  }

  const manifestPath = path.join(SERVER_ROOT, 'build-manifest.json');
  if (!fs.existsSync(manifestPath)) {
    errors.push('[build] build-manifest.json missing — run stamp-build-meta.js');
  }

  const indexPath = path.join(SERVER_ROOT, 'index.html');
  if (fs.existsSync(indexPath)) {
    const indexHtml = fs.readFileSync(indexPath, 'utf8');
    if (!isReactMarketingIndex(indexHtml)) {
      errors.push('[routing] index.html is not the React marketing landing export');
    }
    for (const forbidden of FORBIDDEN_IN_ROOT_INDEX) {
      if (indexHtml.includes(forbidden)) {
        errors.push(`[routing] index.html still contains retired monolith vault hook: ${forbidden}`);
      }
    }
  }

  return errors;
}

function checkReactChunkAssets() {
  const errors = [];
  const htmlFiles = [
    'index.html',
    ...REQUIRED_VAULT_EXPORTS.filter((p) => p.endsWith('.html')),
  ];
  const { missing, assets } = verifyChunkAssets(SERVER_ROOT, htmlFiles);
  if (missing.length) {
    errors.push(`[assets] ${missing.length} _next chunk(s) missing for React HTML exports`);
    missing.slice(0, 8).forEach((rel) => errors.push(`[assets] missing: ${rel}`));
  }
  const appChunks = assets.filter(
    (a) => a.includes('_next/static/chunks/routes/') || a.includes('_next/static/chunks/app/')
  );
  if (appChunks.length === 0) {
    errors.push('[assets] no route chunks found — run merge-into-server.js after client build');
  }
  return errors;
}

async function checkApiProbes() {
  const errors = [];
  const base = (process.env.DEPLOY_GUARDIAN_API_URL || process.env.API_URL || 'https://gatorvault-api.onrender.com').replace(
    /\/$/,
    ''
  );

  for (const probe of CORE_API_PROBES) {
    const url = `${base}${probe.path}`;
    const r = await timedFetch(url);
    if (!r.ok || r.status >= 500) {
      errors.push(`[api] ${probe.id} failed: ${r.error || `HTTP ${r.status}`} (${url})`);
    }
  }

  return errors;
}

async function main() {
  const errors = [];
  const wiring = verifyPlatformWiring({ simulate: true });
  const blueprints = verifyBlueprints({ criticalOnly: true });
  if (!wiring.ok) errors.push(...wiring.errors);
  if (!blueprints.ok) errors.push(...blueprints.errors);
  errors.push(...checkStaticRoutes());
  errors.push(...checkReactChunkAssets());

  if (PROBE_API && !process.argv.includes('--skip-api')) {
    const apiErrors = await checkApiProbes();
    errors.push(...apiErrors);
  }

  const ok = errors.length === 0;
  const result = {
    ok,
    phase: 'pre',
    staticOnly: STATIC_ONLY,
    apiProbed: PROBE_API,
    errors,
    checkedAt: new Date().toISOString(),
  };

  try {
    const deployMonitor = require('../lib/deploy-monitor');
    deployMonitor.recordGuardianCheck(result);
  } catch {
    /* optional */
  }

  if (process.argv.includes('--json')) {
    console.log(JSON.stringify(result, null, 2));
  } else {
    console.log('[deploy-guardian] Pre-deploy —', ok ? 'PASS' : 'FAIL');
    if (errors.length) {
      for (const err of errors) console.error('  ✗', err);
    } else {
      console.log('  ✓ wiring, routes, build manifest' + (PROBE_API ? ', API probes' : ''));
    }
  }

  process.exit(ok ? 0 : 1);
}

main().catch((err) => {
  console.error('[deploy-guardian] fatal:', err.message);
  process.exit(1);
});
